class CircularQueue
  def initialize(int)
    @max_size = int
    @queue_arr = []
  end

  def enqueue(int)
    @queue_arr.shift if @queue_arr.size == @max_size
    @queue_arr << int if @queue_arr.size < @max_size
  end

  def dequeue
    @queue_arr.shift
  end

  def arr
    @queue_arr
  end
end

queue = CircularQueue.new(3)
puts queue.dequeue == nil

queue.enqueue(1)
